<ol>
<li>
<p>框架<br>
猫猫由<code>go-cqhttp_0.9.40</code>+<code>hoshino</code>+<code>yobot</code>作为基本框架部署而成。</p>
</li>
<li>
<p>概述<br>
因为<code>hoshino</code>基于<code>nonebot1</code>(nb1)简单封装而成，并且hoshino在目前主流pcrbot圈子里面比较火，所以目前主流的娱乐插件仅仅支持nb1,因为社区的相对完善，便于维护更新新的娱乐插件，故在这里作为日常主要使用的bot。</p>
</li>
</ol>
<h2 id="momo"><a class="header-anchor" href="#momo">#</a> momo</h2>
<ol>
<li>
<p>框架<br>
