使用 Bot 管理公会战，需要所有成员遵守使用规则，Bot 只是辅助作用，与成员多沟通才能提高分数
:::</p>
<h2 id="初次使用"><a class="header-anchor" href="#初次使用">#</a> 初次使用</h2>
<ol>
<li>在群聊中发送<code>创建国服公会</code>（国、日、台皆可）</li>
<li>所有公会战成员在群聊中发送<code>加入公会</code>，或者由群管理员发送<code>加入全部成员</code></li>
<li>公会战管理员额外进行如下操作：
<ul>
<li>阅读<a href="/guide/yobot_web.md" target="_blank" rel="noopener noreferrer">《会战管理网页后台使用手册》<OutboundLink/></a></li>
<li>向 bot 私聊发送<code>重置密码</code>，使用临时密码进入后台，同时修改登录密码</li>
<li>将网页地址保存到桌面快捷方式（手机、电脑均可），以便使用网页报刀和查看数据</li>
<li>在查刀页面中检查后台记录的成员是否与实际一致，提醒未加入的成员及时加入，并删除非本公会内的无关人员</li>
</ul>
</li>
</ol>
<h2 id="开始前"><a class="header-anchor" href="#开始前">#</a> 开始前</h2>
<ol>
<li>会战管理员登录后台，切换一个新的空白档案来存放本次会战数据，并删除已经离职的公会成员</li>
<li>新成员发送“加入公会”，加入会战管理系统</li>
<li>公会战管理员仔细检查以确认以下事项完成：
<ul>
<li>群内担任会战管理者的成员拥有后台管理权限</li>
<li>后台的公会成员名单与本期参加会战的成员名单一致</li>
<li>切换了空白的会战数据档案</li>
<li>新近入群的公会成员已经熟悉会战功能的操作ul&gt;</li>
</ul>
</li>
</ol>
<h2 id="进行中-成员"><a class="header-anchor" href="#进行中-成员">#</a> 进行中-成员</h2>
<ul>
<li>成员准备挑战 boss 时，需要向 bot 申请挑战，当 bot 允许后开始挑战 boss</li>
<li>如果挑战 boss 完毕，需要向 bot 上报挑战结果</li>
<li>如果挑战 boss 不成功，需要向 bot 上报挂树情况或 SL 使用情况</li>
<li>如果意外掉刀，需要上报一次 0 点伤害的挑战</li>
<li>成员可以使用预约功能，在 boss 出现时获得 bot 的 at 提醒</li>
<li>（可选择执行）如果希望进行合刀筛刀等多人出刀的操作，可以锁定 boss 并留言，等到所有挑战完毕后，按结算顺序向 bot 依次上报</li>
<li>（可选择执行）上报伤害和预约 boss 时，通过留言功能帮助排刀人员制定计划</li>
</ul>
<h2 id="进行中-管理员"><a class="header-anchor" href="#进行中-管理员">#</a> 进行中-管理员</h2>
<ul>
<li>在“查刀”页面中，管理员可以选中当日未完成出刀的成员，一键发送提醒</li>
<li>在“查刀”页面中，排刀人员可以一览所有成员的 SL 使用情况</li>
<li>在“查刀”页面中，排刀人员可以一览所有成员未完成的尾刀</li>
</ul>
<h2 id="特殊情况"><a class="header-anchor" href="#特殊情况">#</a> 特殊情况</h2>
<ul>
<li>如果有人挑战了 boss 却没有上报，可以由他人代理上报</li>
<li>如果前一个上报数据出错，可以由上报者或管理员撤销</li>
<li>如果有人申请了挑战，但没有报伤害也没有报挂树或 SL，其他成员可以在 3 分钟后强制解锁</li>
<li>如果出刀时间在日期变更附近导致未及时上报，需要将伤害上报到前一天的记录中</li>
</ul>
<h2 id="查看数据"><a class="header-anchor" href="#查看数据">#</a> 查看数据</h2>
<p>公会战面板“查刀”页面中能查看记录的所有数据，并进行快速的筛选、过滤。如果需要原始数据，可以从“统计”页面导出。</p>
